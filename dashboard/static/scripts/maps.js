// Construct a Circle Zika.
var constructCirclesZika = function(map){
    $.ajax({
        url: "zika-por-bairro",
        dataType: "json",
        async: false,
        success: function(data) {
            for (var item in data) {
                plotCircle(item, map, data);
            }
        }
    });
}

